export function getLanguage() {
  const saved = localStorage.getItem("pregnova:lang");
  if (saved === "tr" || saved === "en") return saved;
  return navigator.language?.toLowerCase().startsWith("tr") ? "tr" : "en";
}

export function setLanguage(lang) {
  if (lang !== "tr" && lang !== "en") return;
  localStorage.setItem("pregnova:lang", lang);
  window.dispatchEvent(new CustomEvent("pregnova:languageChanged"));
}

export function t(key, params = {}) {
  const lang = getLanguage();
  const value = translations[lang]?.[key] ?? translations.tr[key] ?? key;

  return Object.entries(params).reduce(
    (text, [param, replacement]) =>
      text.replaceAll(`{${param}}`, String(replacement)),
    value
  );
}

export function applyTranslations(root = document) {
  document.documentElement.lang = getLanguage();

  root.querySelectorAll("[data-i18n]").forEach(element => {
    element.textContent = t(element.dataset.i18n);
  });

  root.querySelectorAll("[data-i18n-placeholder]").forEach(element => {
    element.placeholder = t(element.dataset.i18nPlaceholder);
  });

  root.querySelectorAll("[data-i18n-title]").forEach(element => {
    element.title = t(element.dataset.i18nTitle);
  });
}
